#include <iostream>
using namespace std;

int num, sum;

int main() {
	cin >> num;
	sum = 0;
	while (num >= 1) {
		if (num%2 != 0) {
			sum += 1;
		} 
		num /= 2;
	}
	cout << sum;
	
	return 0;
}

